# Circle Application
import math

#Process
radius = 4.2

pi = math.pi

#Formula for the circumference of a circle
circumference = 2 * pi * radius

#Formula for the area of a circle
area = pi * radius ** 2

#Output
print("The circumference is {:.2f} cm, and the area of the circle is {:.2f} cm\u00b2.".format(circumference, area))